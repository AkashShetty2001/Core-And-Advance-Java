package ManytoOne;

//Dependent object
public class Branch {
	
	//instance variables(java bean)
	private String bID;
	private String bName;
	public String getbID() {
		return bID;
	}
	
	//setters and getters
	public void setbID(String bID) {
		this.bID = bID;
	}
	public String getbName() {
		return bName;
	}
	public void setbName(String bName) {
		this.bName = bName;
	}
	
	
	

}
